# Intergers can be in four different forms

# Decimal Form (Base 10: 0 to 9)
a = 100
print("Decimal: 100 ", a)

# Binary Form (Base 2: 0,1) prefixed with 0b or 0B
b = 0B10
print("Binary: 0B10 ", b)

# Octal Form (Base 8: 0 to 7) prefixed with OO or 0o
c = 0O10
print("Octal: 0O10 ", c)

# Hexadecimal Form (Base 16: 0 to 9, a-f) prefixed with 0x or 0X
d = 0X10c
print("Hexadecimal: OX10c ", d )