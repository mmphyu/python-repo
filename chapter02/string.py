# A combination of characters enclosed within single quote or double quote

a = 'Hello World'

b = "Hello World"

print("Single quoted string: ", a)

print("Double quoted string: ", b)

# printing double quoted string

print('Hello, "Mon Myat Phyu"')

# using slice operator on strings

print(a[0])
print(a[-1])
print(a[0:5])

# length of a string
print("String Length: ", len(a))

# type
print("Type of a: ", type(a))